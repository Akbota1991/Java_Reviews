package week06_review;

public class NestedLoopPractice {

    public static void main(String[] args) {


        for (int m = 2; m >= 0 ; m--) {

            for (int s = 59; s >= 0; s--) {
                System.out.println(m +" minutes "+ s +" seconds" );
            }

        }







    }

}
